# La criptografía simétrica es un enfoque en el cual tanto el emisor como el receptor utilizan la misma clave secreta para cifrar y descifrar los datos. 
# Es uno de los métodos más utilizados debido a su eficiencia y rapidez. 
# Sin embargo, uno de los principales desafíos es la gestión segura de la clave, ya que si la clave es comprometida, toda la comunicación se ve vulnerada
